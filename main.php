	<h3>1. SISTEM YANG DIGUNAKAN  </h3>
<p><b>Sistem pakar (<i>expert system</i>) </b>adalah sistem yang berusaha mengapdosi pengetahuan manusia ke komputer, agar komputer dapat menyelesaikan masalah seperti yang biasa dilakukan oleh para ahli. Sistem pakar yang baik dirancang agar dapat menyelesaikan suatu permasalahan tertentu dengan meniru kerja dari para ahli.
</p><br/>

	<h3>2. METODE YANG DIGUNAKAN</h3>
<p>Aplikasi sistem pakar yang dibuat menggunakan metode Naive Bayes dikarenakan metode Naive Bayes merupakan metode klasifikasi terbaik dengan probabilitas yang tinggi ketika digunakan dalam perhitungan sistem nya.
<br><br>
Dengan aplikasi ini seseorang akan merasa seperti sedang berkonsultasi dengan seorang dokter atau pakar yang menangani penyakit ISPA. Aplikasi ini dibangun berbasis website dengan menggunakan bahasa pemrograman PHP Native dan database MySQL.
</p><br/>

 	<h3>3. STUDI KASUS PENELITIAN</h3>
<p>ISPA (Infeksi Saluran Pernafasan Akut) adalah suatu penyakit yang timbul akibat gangguan saluran pernafasan yang dapat menyebabkan berbagai macam penyakit mulai dari penyakit tanpa gejala, infeksi ringan hingga berat akibat dari faktor lingkungan.
<br><br>
Kurangnya pengetahuan masyarakat mengenai gejala, cara penanganan serta cara mengatasi nya, mengakibatkan angka kematian yang cukup tinggi akibat ISPA.</p><br>

 	<h3>4. MANFAAT SISTEM PAKAR</h3>
<p>Sistem pakar ini dibuat diperuntukkan untuk memudahkan seseorang dalam melakukan diagnosa penyakit ISPA dengan cara mengadopsi pengetahuan manusia ke dalam sistem komputer sehingga sistem pakar mampu menyelesaikan permasalahan seperti yang dilakukan seorang pakar.
<br><br>
